import Image from "next/image";
import type { HomeExperience } from "../../types/home";
import { SearchIcon } from "@heroicons/react/solid";
import { Menu, Transition } from "@headlessui/react";
import { Fragment, useEffect, useState } from "react";
import { ChevronDownIcon } from "@heroicons/react/solid";
import LaunchExperienceModal from "./LaunchExperienceModal";

interface PropTypes {
  homeExperiences: HomeExperience[];
}

export default function HomeMain({ homeExperiences }: PropTypes) {
  const [open, setOpen] = useState(true);
  const [launchedHomeExperience, setLaunchedHomeExperience] =
    useState<HomeExperience | null>(null);

  // If a home experience is chosen, show the modal
  useEffect(() => {
    if (launchedHomeExperience) {
      setOpen(true);
    }
  }, [launchedHomeExperience]);

  // If the user clicks out of the modal, set the launched home experience to null
  useEffect(() => {
    if (!open) {
      setLaunchedHomeExperience(null);
    }
  }, [open]);

  return (
    <div className="w-3/4  bg-opacity-30 ">
      <LaunchExperienceModal
        homeExperience={launchedHomeExperience}
        open={open}
        setOpen={setOpen}
      />
      {/* Top element banner */}
      <button className="h-72 w-full relative transition ease-in-out hover:scale-105 duration-300">
        <Image
          src="/TopBannerMETAHOME.png"
          alt="Top banner metahome"
          layout="fill"
          objectFit="contain"
        />
      </button>

      {/* Main section below */}
      <div className="mt-4">
        <div className="flex justify-between w-full items-center">
          {/* Search Icon*/}

          <div className="relative text-gray-400  w-[25rem]">
            <div className="pointer-events-none absolute inset-y-0 left-0 pl-3 flex items-center">
              <SearchIcon className="h-5 w-5" aria-hidden="true" />
            </div>
            <input
              id="search"
              autoComplete="off"
              className="block w-full bg-profile-bar border-opacity-40 pr-4 bg-opacity-40 py-2 pl-10 border-2 border-[#36204E] rounded-md leading-5 text-gray-200 placeholder-gray-500 focus:outline-none  focus:ring-offset-2 focus:ring-offset-indigo-600  focus:border-gray-300 text-sm md:text-lg"
              placeholder="Search"
              type="search"
              name="search"
            />
          </div>

          {/* Filter Dropdown */}
          <Filters />
        </div>

        {/* Home Experiences*/}

        <div className="grid grid-cols-3 grid-rows-1 gap-8 relative mt-8">
          {homeExperiences.map((homeExperience, i) => (
            <button
              key={i}
              onClick={() => setLaunchedHomeExperience(homeExperience)}
              className="group block w-full aspect-w-10 aspect-h-7 rounded-lg focus-within:ring-2 focus-within:ring-offset-2 focus-within:ring-offset-gray-100 focus-within:ring-indigo-500 overflow-hidden transition ease-in-out hover:scale-110 duration-300"
            >
              <img
                src={homeExperience.image}
                alt={homeExperience.name}
                className="object-cover pointer-events-none group-hover:opacity-75"
              />
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}

